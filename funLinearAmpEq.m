% 线性幅度特性滤波器逼近公式
% t=w^2
% IL是在0频率处的理想衰减，也即IL=1-k，k是直线幅度滤波器的斜率
% b是截距
%--------------------------------------------------------------------------
function y = funLinearAmpEq(t, b, IL)
y = 1./((1-IL).*sqrt(t)+b).^2;
end